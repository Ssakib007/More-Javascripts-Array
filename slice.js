const a = [1, 2, 3, 4, 5, 6, 7, 8];

// slicing on an array
console.log(a.slice(1, 5));
// output [ 2, 3, 4, 5 ]
// array.slice(1,5) --> elements will be taken starting from index 1 to upto index 5, but not including index 5.


console.log(a.slice(3));
// output [ 4, 5, 6, 7, 8 ]
// array.slice(3) --> starts removing elements from index 3
